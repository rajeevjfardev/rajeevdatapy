numbers = [1, 2, 3, 4, 5]

# Initialize a variable to store the product with a starting value of 1 (since 1 is the multiplicative identity)
product = 1

# Loop through each number in the list and multiply it to the product variable
for number in numbers:
    product *= number

# Print the total product
print("The product of all the items in the list is:", product)