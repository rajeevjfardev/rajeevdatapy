my_dict = {'name': 'Alice', 'age': 30, 'city': 'New York'}

print("Iterating through keys:")
for key in my_dict:
    print(key)

print("\nIterating through values:")
for value in my_dict.values():
    print(value)

print("\nIterating through key-value pairs:")
for key, value in my_dict.items():
    print(f"{key}: {value}")
